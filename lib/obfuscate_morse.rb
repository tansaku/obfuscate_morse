# frozen_string_literal: true

def morse(string)
  string.split(' ').map do |word|
    word.chars.map do |char|
      "#{MORSE[char]}|"
    end.join.chomp('|')
  end.join('/')
end

def obfuscated_morse(string)
  morse(string).gsub(Regexp.union(OBFUSCATION.keys), OBFUSCATION)
end

OBFUSCATION = {
  '.....' => '5',
  '....' => '4',
  '...' => '3',
  '..' => '2',
  '.' => '1',
  '-----' => 'E',
  '----' => 'D',
  '---' => 'C',
  '--' => 'B',
  '-' => 'A'
}.freeze

